def normalize_dx(D, mode="median", eps=1e-12):
    if mode is None or mode == "none":
        return D
    mask = ~np.eye(D.shape[0], dtype=bool)
    vals = D[mask]
    if mode == "median":
        s = np.median(vals)
    elif mode == "mean":
        s = np.mean(vals)
    elif mode == "fro":
        s = np.linalg.norm(D, ord="fro")
    else:
        raise ValueError("DX_NORM must be: 'none' | 'median' | 'mean' | 'fro'")
    return D / (s + eps)

